// Is `1` string equal (==) to 1 number?
console.log(`1` == 1);
// The answer is true!
// Because the result is 1 for both of them.
// But they have a different data type, so...
 
// ...if you check `1` string === to 1 number?
console.log(`1` === 1);
// The result will be false!
// Because the data type doesn't match too.

//The same happens to the operator different (!= and !==)
console.log(`1` != 1);
console.log(`1` !== 1);

//Also Undefined vs null
console.log(undefined == null);
console.log(undefined === null);

